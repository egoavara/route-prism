// +kubebuilder:printcolumn:name="Age",type=date,JSONPath=`.metadata.creationTimestamp`

// ContextRoute is the Schema for the contextroutes API.
type ContextRoute struct {
	metav1.TypeMeta `json:",inline"`

	// +optional
	metav1.ObjectMeta `json:"metadata,omitzero"`

	// +required
	Spec ContextRouteSpec `json:"spec"`

	// +optional
	Status ContextRouteStatus `json:"status,omitzero"`
}

// +kubebuilder:object:root=true

type ContextRouteList struct {
	metav1.TypeMeta `json:",inline"`
	metav1.ListMeta `json:"metadata,omitzero"`
	Items           []ContextRoute `json:"items"`
}

func init() {
	SchemeBuilder.Register(&ContextRoute{}, &ContextRouteList{})
}
